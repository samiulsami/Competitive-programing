ld f(int i, int j){
    if(ac.trie[j].terminal || ac.trie[j].next_terminal!=-1)return ld(1);
    if(i==l)return 0;
    ld &ret = dp[i][j];
    if(vis[i][j]==color)return ret;
    vis[i][j] = color;

    ret=0;
    for(auto chr:chars){
        ld p = chr.second;
        char c = chr.first;
        ret = ret + p*f(i+1,ac.getnext(j,c));
    }
    return ret;
}

inline void Solve(int Case){
    int k;
    si(k);
    ac = AhoCorasick(k*20);
    for(int i=0; i<k; i++){
        sf(" %s",&s);
        ac.insert(s);
    }

    ac.build();
    int n;
    si(n);
    char c;
    ld p;
    chars.clear();
    for(int i=0; i<n; i++){
        sf(" %c%Lf",&c,&p);
        chars.push_back(pair<char,ld>(c,p));
    }
    si(l);

    color++;
    pf("Case #%d: %0.6Lf\n",Case, ld(1)-f(0,0));
}



