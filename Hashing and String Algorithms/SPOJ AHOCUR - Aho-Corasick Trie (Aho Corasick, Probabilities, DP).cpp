const int _chars=130;

struct AhoCorasick{
    struct trienode{
        int next[_chars]={0};
        int link=0,lvl=0;
        bool terminal=0;
        int next_terminal=-1;
        trienode(int lvl):lvl(lvl){}
    };
    vector<trienode>trie;
    AhoCorasick(int minsz=1e4){
        trie.reserve(minsz);
        trie.emplace_back(0);
    }

    void insert(const char *s){
        int id=0;
        for(int i=0; s[i]; i++){
            int c = s[i];
            if(!trie[id].next[c]){
                trie.emplace_back(trie[id].lvl+1);
                trie[id].next[c]=(int)trie.size()-1;
            }
            id = trie[id].next[c];
        }
        trie[id].terminal=1;
    }

    void build(){
        queue<int>q;
        q.push(0);
        while(!q.empty()){
            int id = q.front();
            q.pop();
            trie[id].next_terminal = (trie[trie[id].link].terminal) ? trie[id].link : trie[trie[id].link].next_terminal;
            for(int c=48; c<130; c++){
                if(trie[id].next[c]){
                    trie[trie[id].next[c]].link = id ? trie[trie[id].link].next[c] : 0;
                    q.push(trie[id].next[c]);
                }
                else trie[id].next[c] = trie[trie[id].link].next[c];
            }
        }
    }

    inline int getnext(int id, char c){
        return trie[id].next[c];
    }
}ac;

ld dp[105][1005];
int vis[105][1005]={0};
