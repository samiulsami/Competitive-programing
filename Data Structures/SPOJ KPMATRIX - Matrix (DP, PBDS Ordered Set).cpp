///   Set.find_by_order(k)  returns a pointer to the k'th element (0 indexed)
///   Set.order_of_key(k)   returns an integer equal to the number of elements STRICTLY SMALLER than 'k'
template<typename T> using orderSet = tree<T,null_type,less<T>,rb_tree_tag,tree_order_statistics_node_update>;
ll mat[251][251]={0};


void solve(int casenum){
	int n,m;
	sii(n,m);
	for(int i=1; i<=n; i++){
		for(int j=1; j<=m; j++){
			sl(mat[i][j]);
			mat[i][j]+=mat[i-1][j];
		}
	}
	
	int a,b;
	sii(a,b);
	orderSet<pll>st;
	ll ans=0;
	ll sum=0;
	
	auto GTE = [&](ll x)->int{
		return sz(st) - st.order_of_key({x,-1});
	};
	
	for(int i=1; i<=n; i++){
		for(int j=i; j<=n; j++){
			st.clear();
			st.insert({0,0});
			sum=0;
			for(int k=1; k<=m; k++){
				sum+=mat[j][k]-mat[i-1][k];
				ans+=GTE(sum-b);
				ans-=GTE(sum-a+1);
				st.insert({sum,k});
			}
		}
	}
	
	pf("%lld\n",ans);
}

